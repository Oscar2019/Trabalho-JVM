uint32_t getQtdOp_tableswitch(const uint8_t* bytecode, uint32_t ind, uint32_t size){
    uint32_t res = 0;
    uint32_t offset = (4 - (ind + 1)%4)%4;
    res += offset;
    res += 4; // Default
    res += 4; // Low
    res += 4; // High
    uint32_t lowS = read<uint32_t>(bytecode + ind + 1 + offset + 4);
    uint32_t highS = read<uint32_t>(bytecode + ind + 1 + offset + 2*4);
    res += 4 * (highS - lowS + 1); // Saltos
    return res;
}

uint32_t getQtdOp_lookupswitch(const uint8_t* bytecode, uint32_t ind, uint32_t size){
    uint32_t res = 0;
    uint32_t offset = (4 - (ind + 1)%4)%4;
    res += offset;
    res += 4; // Default
    res += 4; // Npairs
    res += 8 * static_cast<int32_t>(read<uint32_t>(bytecode + ind + 1 + offset + 4)); // Pairs
    return res;
}

